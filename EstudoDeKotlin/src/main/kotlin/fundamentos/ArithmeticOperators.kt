fun main() {
    var number1 = 10;
    var number2 = 2;
    number2++
    number1--

    println(number1 + number2)
    println(number1 - number2)
    println(number1 / number2)
    println(number1 * number2)
    println(number1 % number2)

    val idade: Float = 10 / 3f
    println(idade)

}